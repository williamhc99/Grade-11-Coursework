class AddInts {
 
  public static void main (String args[]) {
 
    int i = 3;
    int j = 4;
 
    System.out.println("i is " + i);
    System.out.println("j is " + j);
  
    int k = i * j;
    System.out.println("i * j is " + k);
    
    k = i / j;
    System.out.println("i / j is " + k);
 
  }
 
} 